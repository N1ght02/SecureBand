<h2 class="hoofd5">League of Legends Ban</h2>
<p class="whoo">Tyler1 became popular in April of 2016 when his Twitch channel grew famous for Tyler1's bad behavior.
Tyler1 would swear at players and be abusive in chat. He would also leave his keyboard for several hours at a time on stream.
For many League players, Tyler1's most egregious misconduct involved the practice of "inting."
This meant Tyler1 would intentionally allow the opposing players to kill him, giving them money and experience points until they were able to overwhelm his team.
He would often do this in response to other League players not giving him certain buffs he wanted. When he began growing a reputation as "The Most Toxic Player in North America,"
Tyler1's channel drew more viewers, and he began intentionally trolling (making sure the other team wins no mather what) games.
Because Tyler1 would often play with professionals and members of Riot Games' staff, he was quickly noticed. In late April of 2016,
he was banned indefinitely from playing League of Legends. This was a controversial move in the community,
as some players supported Tyler1 while others were glad Riot was looking into the toxic aspects of their communities,
while others still said that Tyler1 was not as bad as some other prominent League players. </p>
